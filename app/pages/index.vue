<!--
================================================================================
[pages/index.vue] 루트 경로 리다이렉트 페이지
================================================================================
앱의 루트 경로('/')에 접근했을 때 정보화 대시보드('/info')로 자동 리다이렉트합니다.
실제 콘텐츠는 없으며, 인라인 미들웨어를 통해 즉시 이동합니다.

[동작 흐름]
  1. 사용자가 '/'에 접근
  2. auth.global 미들웨어 실행 → 인증 체크
  3. 이 페이지의 인라인 미들웨어 실행 → '/info'로 리다이렉트
================================================================================
-->
<script setup lang="ts">
definePageMeta({
  title: '홈',
  middleware: [
    /**
     * 루트 경로('/') 접근 시 정보화 대시보드('/info')로 리다이렉트하는 인라인 미들웨어
     * auth.global 미들웨어 이후 실행됩니다.
     *
     * @param to   - 이동하려는 라우트
     * @param from - 이전 라우트
     */
    function (to, from) {
      if (to.path === '/') {
        return navigateTo('/info')
      }
    }
  ]
})
</script>

<template>
  <!-- 리다이렉트 처리 중 잠깐 표시되는 임시 메시지 -->
  <div>Redirecting...</div>
</template>
